@router.post("/sync-league")
def sync_league(
    req: SoccerdataSyncRequest,
    _admin: dict = Depends(require_admin),
):
    """
    Sync a single league+season from FBref via soccerdata.

    Example body:
        {"league": "Premier League", "season": "2024-2025",
         "data_types": ["fixtures", "stats", "player_stats"]}
    """
    try:
        return _do_sync_league(req.league, req.season, req.data_types or [])
    except ValueError as exc:
        raise HTTPException(status_code=400, detail=str(exc))
    except RuntimeError as exc:
        raise HTTPException(status_code=503, detail=str(exc))
    except Exception as exc:
        raise HTTPException(status_code=500, detail=str(exc))


@router.post("/sync-clubelo")
def sync_clubelo(_admin: dict = Depends(require_admin)):
    """
    Fetch upcoming fixture ELO probabilities from api.clubelo.com/Fixtures
    and store them in team_clubelo — identical to what the browser extension
    does, but triggered from the backend.
    """
    conn = get_connection()
    cur = conn.cursor()
    try:
        rows = _fetch_clubelo_fixtures()
        inserted = _insert_clubelo_rows(cur, rows)
        conn.commit()
        return {"success": True, "team_clubelo_inserted": inserted}
    except httpx.HTTPError as exc:
        conn.rollback()
        raise HTTPException(status_code=502,
                            detail=f"ClubElo API unreachable: {exc}")
    except Exception as exc:
        conn.rollback()
        raise HTTPException(status_code=500, detail=str(exc))
    finally:
        conn.close()


def _sync_all_bg(
    leagues: list[str],
    season: str,
    data_types: list[str],
) -> None:
    """Background task: iterate through leagues sequentially."""
    for league in leagues:
        try:
            result = _do_sync_league(league, season, data_types)
            logger.info("sync-all [%s]: %s", league, result)
        except Exception as exc:
            logger.error("sync-all [%s] failed: %s", league, exc, exc_info=True)


@router.post("/sync-all")
def sync_all(
    req: SoccerdataSyncAllRequest,
    background_tasks: BackgroundTasks,
    _admin: dict = Depends(require_admin),
):
    """
    Kick off a background sync for every configured league (or a subset).
    Returns immediately; progress is visible in server logs.

    Example body:
        {"season": "2024-2025"}
        {"season": "2024-2025", "leagues": ["Premier League", "Bundesliga"]}
    """
    leagues = req.leagues or ALL_CONFIGURED_LEAGUES
    unknown = [lg for lg in leagues if lg not in ALL_LEAGUES]
    if unknown:
        raise HTTPException(
            status_code=400,
            detail=f"Unknown leagues (not in supported leagues map): {unknown}",
        )

    data_types = req.data_types or ["fixtures", "stats", "player_stats"]
    background_tasks.add_task(_sync_all_bg, leagues, req.season, data_types)
    return {
        "success": True,
        "message": f"Background sync started for {len(leagues)} league(s)",
        "leagues": leagues,
        "season": req.season,
        "data_types": data_types,
    }


@router.get("/leagues")
def list_supported_leagues(_admin: dict = Depends(require_admin)):
    """Return the list of leagues supported by the soccerdata sync.

    'native' leagues work out of the box. 'extended' leagues require the
    league_dict.json config file (auto-written on first sync attempt).
    """
    return {
        "leagues": [
            {
                "canonical": k,
                "soccerdata_id": v,
                "support": "native" if k in CANONICAL_TO_SD else "extended",
            }
            for k, v in sorted(ALL_LEAGUES.items())
        ]
    }
