def _schedule_rows_to_fixtures(records: list[dict]) -> list[dict]:
    """
    Convert soccerdata FBref schedule records to the dict format that
    _insert_fixtures() already understands.
    """
    out = []
    for r in records:
        home = str(_val(r, "home_team", "home") or "").strip()
        away = str(_val(r, "away_team", "away") or "").strip()
        if not home or not away:
            continue

        h_goals = _val(r, "home_goals", "score_home")
        a_goals = _val(r, "away_goals", "score_away")
        try:
            score_str = f"{int(h_goals)}–{int(a_goals)}" if (h_goals is not None and a_goals is not None) else ""
        except (ValueError, TypeError):
            score_str = ""

        day_raw = str(_val(r, "day", "") or "")[:3].capitalize()
        round_val = _val(r, "round", "gameweek", "wk")

        out.append({
            "home_team":  home,
            "away_team":  away,
            "date":       str(_val(r, "date", "") or ""),
            "start_time": str(_val(r, "time", "start_time", "") or "") or None,
            "score":      score_str,
            "gameweek":   safe_num(round_val),
            "dayofweek":  _DOW_MAP.get(day_raw),
            "venue":      str(_val(r, "venue", "") or "").strip(),
            "attendance": safe_num(_val(r, "attendance")),
            "referee":    str(_val(r, "referee", "") or "").strip(),
            "round":      str(round_val or "").strip(),
        })
    return out


def _team_stat_rows_to_squad_stats(records: list[dict]) -> list[dict]:
    """
    Convert soccerdata FBref team-season-stats records to the dict format
    that _insert_squad_stats() already understands.

    soccerdata uses the same FBref data-stat attribute names that sync.py
    already handles (players_used, avg_age, possession, games, …).
    Extra columns are forwarded as standard_stats JSON.
    """
    CORE = {"league", "league_id", "season", "team", "squad",
            "players_used", "avg_age", "possession", "games", "games_starts",
            "minutes", "minutes_90s", "goals", "assists"}
    out = []
    for r in records:
        team = str(_val(r, "team", "squad") or "").strip()
        if not team or team.lower() in ("squad", "team", ""):
            continue

        extra = {k: v for k, v in r.items() if k not in CORE and v is not None}
        out.append({
            "team":           team,
            "players_used":   _val(r, "players_used"),
            "avg_age":        _val(r, "avg_age"),
            "possession":     _val(r, "possession"),
            "games":          _val(r, "games", "mp"),
            "games_starts":   _val(r, "games_starts", "starts"),
            "minutes":        _val(r, "minutes", "min"),
            "minutes_90s":    _val(r, "minutes_90s", "90s"),
            "goals":          _val(r, "goals", "gls"),
            "assists":        _val(r, "assists", "ast"),
            "standard_stats": extra,
        })
    return out


def _player_stat_rows_to_player_stats(records: list[dict]) -> list[dict]:
    """
    Convert soccerdata FBref player-season-stats records to the dict format
    that _insert_player_stats() already understands.
    """
    CORE = {"league", "league_id", "season", "player", "team", "squad",
            "nationality", "nation", "position", "pos", "age", "born",
            "games", "games_starts", "minutes", "minutes_90s", "goals", "assists"}
    out = []
    for r in records:
        name = str(_val(r, "player") or "").strip()
        if not name or name.lower() in ("player", ""):
            continue

        raw_nat = str(_val(r, "nationality", "nation") or "").strip()
        nationality = raw_nat.split()[-1] if raw_nat else ""

        extra = {k: v for k, v in r.items() if k not in CORE and v is not None}
        out.append({
            "player":         name,
            "nationality":    nationality[:10],
            "position":       str(_val(r, "position", "pos") or "")[:20],
            "team":           str(_val(r, "team", "squad") or "").strip(),
            "age":            safe_num(_val(r, "age")),
            "birth_year":     safe_num(_val(r, "born", "birth_year")),
            "games":          safe_num(_val(r, "games", "mp")),
            "games_starts":   safe_num(_val(r, "games_starts", "starts")),
            "minutes":        safe_num(_val(r, "minutes", "min")),
            "minutes_90s":    safe_num(_val(r, "minutes_90s", "90s")),
            "goals":          safe_num(_val(r, "goals", "gls")),
            "assists":        safe_num(_val(r, "assists", "ast")),
            "standard_stats": extra,
        })
    return out


