CLUBELO_FIXTURES_URL = "http://api.clubelo.com/Fixtures"


def _fetch_clubelo_fixtures() -> list[dict]:
    """
    Fetch upcoming fixture probabilities from api.clubelo.com/Fixtures.
    Returns a list of dicts matching what sync_enrichment's clubelo insert
    logic already expects (keys: Home, Away, League, Date, Home_Elo, Away_Elo,
    plus the probability columns stored as raw_data).
    """
    response = httpx.get(CLUBELO_FIXTURES_URL, timeout=30)
    response.raise_for_status()

    reader = csv.DictReader(io.StringIO(response.text))
    rows = []
    for row in reader:
        rows.append({
            "League": row.get("League", ""),
            "raw": {
                "Home":     row.get("HomeTeam", row.get("Home", "")),
                "Away":     row.get("AwayTeam", row.get("Away", "")),
                "Date":     row.get("Date", ""),
                "Home_Elo": row.get("Home", ""),
                "Away_Elo": row.get("Away", ""),
                **row,
            },
        })
    return rows


def _insert_clubelo_rows(cur, rows: list[dict]) -> int:
    """
    Insert ClubElo fixture rows into team_clubelo.

    Uses the same league-resolution strategy as sync_enrichment.py:
    look up each team's REAL league from the teams table rather than
    trusting ClubElo's country code (which uses ENG for ALL English
    divisions, causing Premier League / Championship / League One /
    League Two to be mixed).
    """
    from routes.sync_enrichment import _normalize_team_name, _strip_affixes
    count = 0
    for row in rows:
        raw = row.get("raw", row)
        home = raw.get("Home") or raw.get("HomeTeam")
        away = raw.get("Away") or raw.get("AwayTeam")
        target_date = raw.get("Date")

        if not home or not away or not target_date:
            continue

        home_elo = safe_num(raw.get("Home_Elo") or raw.get("Home_Prob"))
        away_elo = safe_num(raw.get("Away_Elo") or raw.get("Away_Prob"))

        for team_name, elo_val in [(home, home_elo), (away, away_elo)]:
            if not team_name:
                continue

            clean = _normalize_team_name(team_name)
            cur.execute(
                "SELECT id FROM teams WHERE name ILIKE %s LIMIT 1",
                (clean,)
            )
            team_row = cur.fetchone()

            if not team_row:
                stripped = _strip_affixes(clean)
                if stripped and stripped != clean.lower():
                    cur.execute(
                        "SELECT id FROM teams WHERE name ILIKE %s LIMIT 1",
                        (f"%{stripped}%",)
                    )
                    team_row = cur.fetchone()

            if not team_row:
                logger.debug("ClubElo: team %r not found in DB, skipping", clean)
                continue

            t_id = team_row["id"]
            cur.execute(
                """
                INSERT INTO team_clubelo (team_id, elo_date, elo, raw_data)
                VALUES (%s, %s, %s, %s)
                ON CONFLICT (team_id, elo_date) DO UPDATE SET
                    elo      = EXCLUDED.elo,
                    raw_data = EXCLUDED.raw_data,
                    scraped_at = NOW()
                """,
                (t_id, target_date, elo_val, json.dumps(row)),
            )
            count += 1
    return count


