_DOW_MAP = {
    "Mon": 1, "Tue": 2, "Wed": 3, "Thu": 4,
    "Fri": 5, "Sat": 6, "Sun": 7,
}


def _val(row: dict, *keys, default=None):
    """Return the first non-None value found under any of the given keys."""
    for k in keys:
        v = row.get(k)
        if v is not None and str(v).strip() not in ("", "nan", "NaT", "None"):
            return v
    return default


def _flatten_df(df: pd.DataFrame) -> pd.DataFrame:
    """Flatten multi-level column headers produced by soccerdata for stat tables."""
    if isinstance(df.columns, pd.MultiIndex):
        df = df.copy()
        df.columns = [
            "_".join(str(c) for c in col if str(c) not in ("", "nan")).lower()
            for col in df.columns
        ]
    else:
        df = df.copy()
        df.columns = [str(c).lower() for c in df.columns]
    return df


def _df_to_records(df: pd.DataFrame) -> list[dict]:
    """Reset index, flatten columns, replace NaN with None, return list of dicts."""
    df = df.reset_index()
    df = _flatten_df(df)
    df = df.where(pd.notna(df), None)
    return df.to_dict("records")


